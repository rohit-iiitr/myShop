export const PaymentController = async (req, res) => {
  try {
    const { cart } = req.body;

    if (!Array.isArray(cart) || cart.length === 0) {
      return res.status(400).json({ error: "Cart is empty or invalid." });
    }

    let total = 0;
    cart.forEach((item, i) => {
      if (typeof item.price !== "number") {
        throw new Error(`Invalid or missing price at cart[${i}]`);
      }
      total += item.price;
    });

    const paymentIntent = await stripe.paymentIntents.create({
      amount: Math.round(total * 100), // cents
      currency: "usd",
      automatic_payment_methods: { enabled: true },
    });

    res.status(200).json({
      success: true,
      clientSecret: paymentIntent.client_secret,
    });
  } catch (err) {
    console.error("❌ Stripe Payment Error:", err);
    res.status(500).json({ error: "Payment failed", message: err.message });
  }
};


export const PaymentConfirm = async (req, res) => {
  try {
    const { cart, payment } = req.body;

    if (!Array.isArray(cart) || cart.length === 0) {
      return res.status(400).json({ error: "Cart is empty or invalid." });
    }

    // ✅ Recalculate total
    let total = 0;
    cart.forEach((item) => {
      if (typeof item.price !== "number") {
        throw new Error("Invalid price in cart item.");
      }
      total += item.price;
    });

    // ✅ Verify payment status with Stripe
    const verifiedPayment = await stripe.paymentIntents.retrieve(payment.id);
    if (verifiedPayment.status !== "succeeded") {
      return res.status(400).json({ error: "Payment not confirmed." });
    }
    if (verifiedPayment.amount !== Math.round(total * 100)) {
      return res.status(400).json({ error: "Payment amount mismatch." });
    }

    // ✅ Create order
    const order = new orderModel({
      products: cart.map((item) => item._id),
      payment: {
        id: payment.id,
        amount: verifiedPayment.amount,
        success: true,
      },
      status: "Not Process",
      ...(req.user?._id && { buyer: req.user._id }),
    });

    await order.save();

    res.status(200).json({
      success: true,
      message: "Order placed successfully.",
    });
  } catch (err) {
    console.error("❌ PaymentConfirm Error:", err.message);
    res.status(500).json({
      success: false,
      error: "Server Error",
      message: err.message,
    });
  }
};
